package ncg.statistics;

/*
 * Exception thrown by the DiscriminantAnalysisGUI class
 */
public class DiscriminantAnalysisGUIException extends Exception {

	// constructor signatures all match constructors of the Exception class
	
	public DiscriminantAnalysisGUIException() {
		super();
	}
	
	public DiscriminantAnalysisGUIException(String message) {
		super(message);
	}
	
	public DiscriminantAnalysisGUIException(String message, Throwable cause) {
		super(message,cause);
	}
	
	public DiscriminantAnalysisGUIException(Throwable cause) {
		super(cause);
	}
	
	
}

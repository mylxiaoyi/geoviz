package ncg.statistics;

/*
 * Exception thrown by the DiscriminantAnalysis class
 */
public class DiscriminantAnalysisException extends Exception {

	// constructor signatures all match constructors of the Exception class
	
	public DiscriminantAnalysisException() {
		super();
	}
	
	public DiscriminantAnalysisException(String message) {
		super(message);
	}
	
	public DiscriminantAnalysisException(String message, Throwable cause) {
		super(message,cause);
	}
	
	public DiscriminantAnalysisException(Throwable cause) {
		super(cause);
	}
	
	
}
